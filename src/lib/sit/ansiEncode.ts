/**
 * Codifica string como Windows-1252 (ANSI) — formato exigido pelo SIT.
 * A função formatLinhaSIT já remove acentos, então o resultado é
 * praticamente ASCII; caracteres fora do mapeamento viram '?'.
 */

// Mapeamento dos 32 caracteres especiais do bloco 0x80-0x9F do win1252.
const WIN1252_EXTRA: Record<number, number> = {
  0x20ac: 0x80, 0x201a: 0x82, 0x0192: 0x83, 0x201e: 0x84,
  0x2026: 0x85, 0x2020: 0x86, 0x2021: 0x87, 0x02c6: 0x88,
  0x2030: 0x89, 0x0160: 0x8a, 0x2039: 0x8b, 0x0152: 0x8c,
  0x017d: 0x8e, 0x2018: 0x91, 0x2019: 0x92, 0x201c: 0x93,
  0x201d: 0x94, 0x2022: 0x95, 0x2013: 0x96, 0x2014: 0x97,
  0x02dc: 0x98, 0x2122: 0x99, 0x0161: 0x9a, 0x203a: 0x9b,
  0x0153: 0x9c, 0x017e: 0x9e, 0x0178: 0x9f,
};

export function encodeWin1252(text: string): Uint8Array {
  const out = new Uint8Array(text.length);
  for (let i = 0; i < text.length; i++) {
    const cp = text.charCodeAt(i);
    if (cp < 0x80 || (cp >= 0xa0 && cp <= 0xff)) {
      out[i] = cp;
    } else if (WIN1252_EXTRA[cp] != null) {
      out[i] = WIN1252_EXTRA[cp];
    } else {
      out[i] = 0x3f; // '?'
    }
  }
  return out;
}
